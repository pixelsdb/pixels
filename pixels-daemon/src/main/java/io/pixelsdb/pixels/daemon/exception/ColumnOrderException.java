public class ColumnOrderException extends Exception
{
    public ColumnOrderException(String message)
    {
        super (message);
    }

    @Override
    public String getMessage ()
    {
        return super.getMessage();
    }

    @Override
    public String toString ()
    {
        return super.toString();
    }
}
